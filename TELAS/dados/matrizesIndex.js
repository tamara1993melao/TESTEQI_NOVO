// Monta deck embaralhado limitado ao que existe (sem repetir)
export function buildMatrizDeck(n = 12) {
  const total = MATRIZES.length;
  const want = Math.min(n, total);
  if (n > total) {
    console.log(`[MATRIZES][local] solicitado ${n} mas sÃ³ existem ${total}. Usando ${want}.`);
  }
  const pool = MATRIZES.slice();
  for (let i = pool.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [pool[i], pool[j]] = [pool[j], pool[i]];
  }
  return pool.slice(0, want);
}